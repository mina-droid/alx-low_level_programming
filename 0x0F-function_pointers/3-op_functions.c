/**
*op_add-function
*@a:intger 1
*@b:intger 2
*Return: intger
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
*op_sub-function
*@a:intger 1
*@b:intger 2
*Return: intger
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
*op_mul-function
*@a:intger 1
*@b:intger 2
*Return: intger
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
*op_div-function
*@a:intger 1
*@b:intger 2
*Return: intger
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
*op_mod-function
*@a:intger 1
*@b:intger 2
*Return: intger
*/

int op_mod(int a, int b)
{
return (a % b);
}
